"""Проектные константы и простые типы для игры в шахматы.

Этот модуль содержит визуальные константы (размеры окна, цвета, параметры анимации),
псевдонимы типов и таблицу юникод-символов для отрисовки фигур, используемых
в Renderer как резервный вариант, когда спрайты отсутствуют.

Пример использования:
    from src.config import SQUARE_SIZE, UNICODE_PIECES, Pos
    pos: Pos = (0, 0)
    square_px = (pos[1] * SQUARE_SIZE, pos[0] * SQUARE_SIZE)
"""

from typing import Tuple

# Окно и сетка
WIDTH, HEIGHT = 640, 640  # размер окна в пикселях (ширина, высота)
ROWS, COLS = 8, 8  # число рядов и колонок на шахматной доске
SQUARE_SIZE = WIDTH // COLS  # размер стороны клетки в пикселях

# Производительность / анимация
FPS = 60  # целевой FPS
ANIMATION_DURATION = 250  # длительность анимации хода в миллисекундах

# Цвета (RGB)
LIGHT = (240, 217, 181)  # светлая клетка
DARK = (181, 136, 99)  # тёмная клетка
HIGHLIGHT = (246, 246, 105)  # подсветка для выбранной клетки
MOVE_MARK = (200, 50, 50)  # цвет маркера возможного хода
BLACK_TEXT = (20, 20, 20)  # цвет текста/глифов

# Unicode-символы фигур — ключ (color, kind)
# Используется как fallback, если отсутствуют картинковые спрайты.
UNICODE_PIECES = {
    ('w', 'K'): '♔', ('w', 'Q'): '♕', ('w', 'R'): '♖', ('w', 'B'): '♗', ('w', 'N'): '♘', ('w', 'P'): '♙',
    ('b', 'K'): '♚', ('b', 'Q'): '♛', ('b', 'R'): '♜', ('b', 'B'): '♝', ('b', 'N'): '♞', ('b', 'P'): '♟',
}

# Простое алиас-тип для координат (row, col)
Pos = Tuple[int, int]
